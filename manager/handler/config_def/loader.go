package config_def

import (
	"encoding/json"
	"module-manager/manager/itf"
	"os"
)

func Load(path string) (map[string]itf.ConfigDefinition, error) {
	file, err := os.Open(path)
	if err != nil {
		return nil, err
	}
	defer file.Close()
	decoder := json.NewDecoder(file)
	var d map[string]itf.ConfigDefinition
	if err = decoder.Decode(&d); err != nil {
		return nil, err
	}
	return d, nil
}
